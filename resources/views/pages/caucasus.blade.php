                        <b
                          >DAY 8: YEREVAN – KHOR VIRAP – ECHMIADZIN – ZVARTNOTS
                          – YEREVAN </b
                        ><br />After breakfast at the hotel, head to the region
                        Ararat to visit Khor Virap, situated close-by the river
                        Araks. The importance of this monastery is connected
                        with the first Armenian Catholicos Gregori Illuminator
                        who made Christianity the state religion. Then the road
                        will take us to Echmiadzin – the mother church of the
                        Armenian Apostolic Church and the holy seat of the
                        Supreme Patriarch-the Catholicos. Echmiadzin Cathedral
                        was constructed in 303, and is one of the unique
                        architectural constructions of the IV century. On the
                        way back we will visit the ruins of Zvartnots. The
                        temple was constructed in the VII century and destroyed
                        by powerful earthquake in the X century. Both Zvartnots
                        temple and Echmiadzin Cathedral are included on the
                        UNESCO Cultural Heritage list. Overnight in Yerevan.
                      </p>
