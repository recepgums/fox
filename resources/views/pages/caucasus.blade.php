                        of the Upland Park. The tour covers the central part of
                        Baku showing of the most interesting historical and
                        architectural sites, as well as visiting a favorite
                        walking place for residents and guests - Baku Boulevard.
                        Also, we will visit the "Old Town", there are several
                        dozen historical and architectural monuments are
                        situated in its the territory, including the Palace of
                        the Shirvanshahs, the symbol of Baku - Maiden Tower,
                        places of rest and stay of medieval merchants -
                        Caravansarai. Lunch. We continue our tour by bus to the
                        central part of Baku in order to see the most
                        interesting historical and architectural monuments.Our
                        today's tour ends with a visit to the cultural complex
                        named after Heydar Aliyev. The complex has several
                        exhibitions that can be visited at the choice of the
                        tourist (surcharge). Visit Fire Mountain. Transfer for
                        dinner Return to the hotel. Overnight in Baku.
                      </p>
                      <p>
                        <b>DAY 3: BAKU – SHAMAKHA – SHEKI </b><br />08:00
                        Breakfast in the hotel. Check out. Drive in the
                        North-West of Azerbaijan. The road to the city of Sheki
                        will pass on one of the most beautiful corners of
                        Azerbaijan at the foot of the Great Caucasus. Visiting
                        Juma Mosque Shamaka. Stop for Lunch 16:00 Arrival to
                        Sheki, check-in. Rest. We’ll start our Sheki tour from
                        visiting the 18th century Khan's summer palace with
                        magnificent frescos and exquisite stained-glass work. We
                        will visit “Shebeke” workshop where the tourists will
                        meet with local craftsman who will tell about “Shebeke”
                        make process. After we will also visit workshop of Sheki
                        baklava that is popular all over Azerbaijan. Dinner.
                        Overnight in Sheki.
