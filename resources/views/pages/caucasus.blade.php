                        the lake Sevan, which is the second highest freshwater
                        lake in the world (about 2000 meters above the sea
                        level). Being endowed with incomparable beauty, Sevan
                        has turned into one of the main tourist attractions of
                        Armenia. Reaching the peninsula of Sevan, we will climb
                        the stairs to Sevanavank Monastery, founded by Gregory
                        Illuminator in 305 and enjoy the view to the blue-eyed
                        lake from above the hill. Afterwards, we will head to
                        Dilijan, also known as “Armenian Switzerland” due to its
                        fabulous nature, surrounded by dense forests and rocky
                        mountains. Here we will visit Goshavank Monastery,
                        situated in Gosh village on a green hill. The monastery
                        was founded in the 12th century and used to be an
                        educational center. You will find a unique Khachkar
                        (cross-stone) under the wall of the church, which was
                        created with old simple instruments. You will be
                        enchanted by the amazing construction of the monastery
                        and the stunning nature of Dilijan. After a tour in this
                        wonderful area, you will be transported to the
                        Armenia-Georgia border to continue your trip. Transfer
                        to Tbilisi, have a dinner and overnight at the hotel.
