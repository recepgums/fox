                        will see the most beautiful sights of the capital, like
                        The Republic Square, where you may see the stone carpet
                        right in the middle, hiding history of 3000 years
                        underneath. Directly from the Republic Square opens the
                        Northern Avenue, which represents the modern part of the
                        city, linking the Republic Square with Freedom Square.
                        The huge building of the Opera and Ballet Theatre is
                        located here. Afterwards you will admire the enchanting
                        Cascade Monument and Mother Armenia Monument. Also, you
                        will visit the Genocide memorial to get closer to one of
                        the saddest pages of the Armenian history. Afterwards,
                        you will drive to the only preserved Pagan Temple in the
                        whole Caucasus area Garni (1st century). You will
                        continue your way to Geghard Monastery, part of which
                        was carved right inside the cliff in the 4th century.
                        During your lunch you will participate in a master class
                        of baking Armenian traditional bread “Lavash”. Geghard
                        is in the list of UNESCO Heritage Drive back to Yerevan.
                        Overnight in Yerevan.
