                        Bazaar where you will be able to buy fruits and
                        souvenirs and see how local people trade and
                        deal.Transfer to Georgian boarder, pass the border
                        formalities, change the guide and driver. Starting
                        exploring Georgia with the land of wine – Kakheti
                        region. First we will visit Sighanghi, “city of love”.
                        Excursion in this beautiful city, visit city fortress
                        walls overlooking Alazany valley with stunning views.
                        Lunch at the local restaurant and wine degustation at
