                      <p>
                        <b>DAY 5: TBILISI CITY TOUR</b><br />Breakfast at the
                        hotel. Meeting guide at the hotel reception. Excursion
                        in the capital city of Georgia. Founded by Vakhtang
                        Gorgasali, the Georgian King of Kartli (Iberia) and made
                        into a capital in the 6th century, Tbilisi is a
                        significant industrial, social, and cultural center.
                        Visiting following sights: Tbilisi Sameba Cathedral, the
                        largest cathedral in the Caucasus, Metekhi Church is
                        erected on the cliff, overlooking the Mtkvari River. The
                        colorful Darejani Palace is also situated on the cliff,
                        like the nest of a swallow. The Sulfur Baths are located
                        in Old Tbilisi, on the other side of the river, opposite
                        Darejani Palace. Narikala fortress, established in the
                        fourth century. Lunch at a local restaurant. Then
                        continue tour by visiting Shardeni street with its
                        fashionable, cozy cafes and art galleries. Visit famous
                        Bridge of Peace. Visit Rustaveli Avenue, main street of
                        the city, passing along the attractions, such as Tbilisi
                        Opera and Ballet theatre, Rustaveli Theatre, different
                        art galleries and cafes. Dinner at the restaurant.
                        Transfer and overnight in Tbilisi.
                      </p>
