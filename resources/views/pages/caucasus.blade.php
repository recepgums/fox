                        city was founded by one of the greatest kings of
                        Georgia, David the Builder (1089–1125). Visit Joseph
                        Stalin Museum in here. Lunch at a local restaurant.
                        Afterwards drive to visit Uplistsikhe, "the lord's
                        fortress", which is an ancient rock-hewn town in eastern
                        Georgia. Built on a high rocky left bank of Mtkvari
                        River, it contains various structures dating from the
                        2nd ML BC to the Late Middle Ages, and is notable for
                        the unique combination of various styles of rock-cut
                        cultures. Drive back to Tbilisi. Transfer to Georgian
                        restaurant and have a dinner with folk – show.
