                      <h4>5N6D MOSCOW & ST PETERSBURG TOUR BY FOX DMC</h4>
                      <p>
                        <b>DAY 1 HOME - MOSCOW</b><br />Arrival to Moscow.
                        Meeting with the guide at the airport. Transfer to
                        hotel. Overnight in Moscow.
                      </p>
                      <p>
                        <b>DAY 2 MOSCOW</b><br />Breakfast at the hotel. Meeting
                        with your guide at the hotel lobby. City Tour included
                        (Zamoskvorechye, New Maiden Convent, The Moscow State
                        University, Sparrow Hills, VDNH) without entrances.
                        Space Museum, it is located within the base of the
                        Monument to the Conquerors of Space. The museum contains
                        a wide variety of Soviet and Russian space-related
                        exhibits and models which explore the history of flight;
                        astronomy; space exploration; space technology; and
                        space in the arts. Lunch at the restaurant. Kremlin
                        territory with cathedrals. The Moscow Kremlin is one of
                        the largest architectural ensembles in the world. It is
                        home to the Moscow Kremlin State Museum of History and
                        Culture. Visit to GUM Department Store. Walking tour at
                        Red Square. Dinner at the restaurant. Transfer to the
                        hotel. Overnight in Moscow.
                      </p>
                      <p>
                        <b>DAY 3 MOSCOW</b><br />Breakfast at the hotel. Meeting
                        with your guide at the hotel lobby. The State Tretyakov
                        Gallery is a museum of world significance, with
                        collections running into many thousands of works of art,
                        from Old Russia icons to contemporary paintings. The
                        collection of the Tretyakov brothers was opened to the
                        public as a private museum in 1874 and it immediately
                        became widely popular. Lunch. Visit to Christ the Savior
                        Cathedral. Enjoy Metro Tour. The first line of Moscow
                        metro was opened in 1935. Now, there are more than 150
                        stations and Moscow metro is the main means of municipal
                        transport in the city. Arbat street walking tour. Dinner
                        at the restaurant. Transfer to the hotel. Overnight in
                        Moscow.
                      </p>
                      <p>
                        <b>DAY 4 MOSCOW-ST. PETERSBURG</b><br />Breakfast at
                        hotel. Meeting with your guide at the hotel lobby. Lunch
                        at the restaurant. Transfer to the railway station.
                        Sapsan train(economic class) to St. Petersburg. Arrival
                        to St. Petersburg. Meeting with your guide at the
                        station. Dinner at the restaurant. Transfer to the
                        hotel. Overnight in St. Petersburg.
                      </p>
                      <p>
                        <b>DAY 5 ST. PETERSBURG</b><br />Breakfast at hotel.
                        City Tour included (Nevskiy Street, St. Isaac’s
                        Cathedral, Palace Square, Spit of Vasilievskiy Island,
                        Cruiser Aurora, Saviour on the Spilled Blood) without
                        entrances Lunch at the restaurant. Hermitage is a museum
                        of art and culture in Saint Petersburg. The
                        second-largest art museum in the world, it was founded
                        in 1764. Dinner at the restaurant. Transfer to the
                        hotel. Overnight in St. Petersburg.
                      </p>
                      <p>
                        <b>DAY 6 ST. PETERSBURG - HOME</b><br />Meeting with
                        your guide at the hotel lobby. Transfer to the airport.
                        Return home with happy memories!
                      </p>
                      <h4>6N7D MOSCOW & ST PETERSBURG TOUR BY FOX DMC</h4>
                      <p>
                        <b>DAY 1 HOME - MOSCOW</b><br />Arrival to Russian
                        Capital, Moscow. Meet with the guide at the airport.
                        Transfer to hotel for check in. Free time at leisure.
                        Overnight in Moscow.
                      </p>
                      <p>
                        <b>DAY 2 MOSCOW</b><br />Breakfast at hotel. Your first
                        full day in Moscow you will have a guided tour of the
                        major cultural and historical sites of the city. We
                        visit Red Square and the Moscow State University on
                        Lenin Hill, where there are outstanding views over
                        Moscow. On the way down, we pass through the Memorial
                        Complex at Poklonnaya Hill and Victory Park, where the
                        impressive Triumphal Arch is sited. Then it's back to
                        the city streets, taking in Kutuzov Avenue, New Arbat,
                        Boulevard Ring and Pushkin Square, before ending up at
                        Tverskaya Street. Guided tour of Moscow’s Underground –
                        explore one of the masterpieces of the Soviet era
                        created in the Socialist Realism style. The Moscow
                        Metro, with its stations decorated with marble, mosaics,
                        stained-glass panels and bronze sculptures, looks more
                        like a vast art museum. It was one of the proudest
                        achievements of the Soviet era and it still beats many
                        other European underground systems with its
                        ultra-reliable service. Overnight in Moscow.
                      </p>
                      <p>
                        <b>DAY 3 MOSCOW </b><br />Breakfast at hotel. This
                        morning you will visit Red Square, arguably one of the
                        most famous squares in the world. It got its name from
                        the Russian word for "red", which many centuries ago
                        meant "beautiful". You will see the brightly-domed St
                        Basil's Cathedral, the world-renowned Lenin Mausoleum,
                        the expansive facade of the GUM department store, Kazan
                        Cathedral and the Kremlin towers. You will discover the
                        interior of the Kremlin - the former residence of the
                        Tsars and now the residence of the Russian President.
                        You will also visit the Kremlin’s beautiful orthodox
                        cathedrals. Cathedral Square still retains the aura of
                        early Tsarist Russia, where the spirits of Ivan the
                        Terrible, Boris Godunov and the early Romanovs loom
                        large. Next, you will explore the breathtaking
                        collection of the Armoury Museum. It contains such
                        notable exhibits as the chalice of Yuri Dolgorukiy, the
                        helm and armour of Boris Godunov, a collection of over
                        fifty Faberge eggs, golden and silver works of applied
                        art by Russian, European and Oriental masters,
                        invaluable weapons and other works made of precious
                        metals and stones. Later, you will be taken back to the
                        hotel. Overnight in Moscow.
                      </p>
                      <p>
                        <b>DAY 4 MOSCOW – ST PETERSBURG </b><br />Breakfast in
                        the hotel and free time in the morning. Check-out before
                        noon. Transfer from the hotel to the train station for
                        your speedy train (4 hours) from Moscow to St
                        Petersburg. Upon arrival to St Petersburg you will be
                        taken to your hotel for check-in. Overnight in St.
                        Petersburg.
                      </p>
                      <p>
                        <b>DAY 5 ST PETERSBURG</b><br />Breakfast at hotel.
                        Today will take you around the stunning "Venice of the
                        North", as St Petersburg is also known. You will see the
                        Peter and Paul Fortress, St Isaac's Cathedral and the
                        Church of the Saviour on Spilled Blood. Then there’s the
                        famous Aurora warship, the legendary ship whose
                        canon-fire gave the signal, on the order of Lenin, for
                        the beginning of the Great Socialist Revolution more
                        than 90 years ago. In the afternoon we head off to one
                        of the greatest and most beautiful art museums in the
                        world - the Hermitage State Museum. The collection of
                        the Hermitage includes more than three million works of
                        art and artefacts of world culture, including Da Vinci,
                        Raphael, Rembrandt, Monet and Michelangelo. Among them
                        are paintings, graphic works, sculptures and works of
                        applied art, archaeological finds and numismatic
                        material. Overnight in St. Petersburg.
                      </p>
                      <p>
                        <b>DAY 6 ST PETERSBURG</b><br />Breakfast at hotel.
                        Enjoy a guided tour to Peterhof Grand Palace & Park in
                        the morning. Peterhof an erstwhile residence of the
                        Czars of yore. Visit the Grand Palace and be amazed with
                        its fascinating & elaborate interiors. This world-famous
                        Palace, Fountain and Park ensemble of Peterhof shines
                        forth as a glittering landmark of Russia's aesthetic
                        culture in the 18th and 19th centuries. Its wonderful
                        parks, 176 fountains of various forms, majestic palaces,
                        numerous gilded statues of ancient gods and heroes,
                        paintings and works of art and culture make Peterhof a
                        veritable gem of art, often called the 'Capital of
                        Fountains,' unique in the world. Overnight in St.
                        Petersburg.
                      </p>
                      <p>
                        <b>DAY 7 ST PETERSBURG – HOME </b><br />Breakfast at
                        hotel. Check-out and transfer to the airport.
                      </p>
